public class Reloj {
    int horas;
    String minutos;
    boolean tarde = true;

    
    public int getHoras() {
        return horas;
    }
    public void setHoras(int horas) {
        this.horas = horas;
    }
    public String getMinutos() {
        return minutos;
    }
    public void setMinutos(String minutos) {
        this.minutos = minutos;
    }
    public boolean isTarde() {
        return tarde;
    }
    public void setTarde(boolean tarde) {
        this.tarde = tarde;
    }
}

